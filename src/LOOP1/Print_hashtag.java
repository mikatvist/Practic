package LOOP1;
/*
Given an int variable k that has already been declared, use a for loop to print a
single line consisting of 49 hashtags. Use no variables other than k.
 */
public class Print_hashtag {
    public static void main(String[] args) {
        int k;
        for(k = 1; k<=49; k++){
            System.out.print("#");
        }
    }
}
